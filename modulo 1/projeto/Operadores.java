class Operadores{
    public static double soma(double n1, double n2){
        return n1 + n2;
    }

    public static double sub(double n1, double n2){
        return n1 - n2;
    }

    public static double div(double n1, double n2){
        return n1 / n2;
    }

    public static double mult(double n1, double n2){
        return n1 * n2;
    }
}
